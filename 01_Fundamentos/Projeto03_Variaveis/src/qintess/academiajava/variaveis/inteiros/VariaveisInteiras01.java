package qintess.academiajava.variaveis.inteiros;

public class VariaveisInteiras01 {
	public static void main(String[] args) {
		// variaveis byte: permitem valores entre -128 a 127
		byte tamanho = 120;
		
		// variaveis short: valores entre -32768 a 32767
		short alunos = 1500;
		
		// variaveis int: valores entre -2147483648 a 2147483647
		int populacao = 650000;
		
		System.out.println("Tamanho informado:" + tamanho);
		System.out.println("Numero de alunos:" + alunos);
		System.out.println("População da minha cidade: " + populacao);
		
	}

}
