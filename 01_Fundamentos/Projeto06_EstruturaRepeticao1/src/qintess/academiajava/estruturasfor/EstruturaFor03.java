package qintess.academiajava.estruturasfor;

public class EstruturaFor03 {
	public static void main(String[] args) {
		
		/*Neste exercicio faremos a apresentação dos números referentes ao
		 * indice de estrutura de repeticão na ordem inversa
		 * 
		 * Aqui vamos escrever de 10 a 0
		 */
		for (int i = 10 ; i > 0 ; i--) {
			System.out.println("Valor da variável i = " + i);
			
		}
	}
	

}
